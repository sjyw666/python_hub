#赋值运算符（=）
#执行顺序  从右到左
i=3+4
print(i)   #打印输出的结果为7，说明先进行等号 右边的计算，再将计算的结果存储到a中

#支持链式赋值   a=b=c=20    a,b,c三个一起引用同一个整数对象
